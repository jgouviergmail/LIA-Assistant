class PlanValidator:
    """
    Validator for agent execution plans.

    Validates a plan generated by the planner before execution by the orchestrator.
    Relies on catalogue manifests for exhaustive validation.

    Validations performed:
    1. Parameters: conformity with ParameterSchema (types, constraints, required)
    2. Permissions: required OAuth scopes, allowed roles
    3. Costs: total budget, cost per step
    4. HITL: detection of tools requiring human approval
    5. Structure: valid steps, existing tools, dependencies

    Attributes:
        registry: AgentRegistry containing manifests

    Examples:
        >>> validator = PlanValidator(registry)
        >>> context = ValidationContext(user_id="user123", session_id="sess456")
        >>> result = validator.validate_plan(plan, context)
        >>> if result.is_valid:
        ...     orchestrator.execute(plan)
        ... else:
        ...     logger.error("plan_invalid", errors=result.errors)
    """

    def __init__(self, registry: AgentRegistry) -> None:
        """
        Initialize the validator.

        Args:
            registry: AgentRegistry with catalogue manifests
        """
        self.registry = registry

    def validate_plan(self, plan: dict[str, Any], context: ValidationContext) -> ValidationResult:
        """
        Validate a complete plan.

        Args:
            plan: Plan generated by planner (format: {"steps": [...]})
            context: Validation context (user, scopes, budget)

        Returns:
            ValidationResult with is_valid, errors, warnings

        Examples:
            >>> plan = {
            ...     "steps": [
            ...         {"tool": "search_contacts_tool", "args": {"query": "John"}},
            ...         {"tool": "get_contact_details_tool", "args": {"resource_name": "$steps.0.contacts[0].resource_name"}}
            ...     ]
            ... }
            >>> result = validator.validate_plan(plan, context)
            >>> assert result.is_valid
        """
        result = ValidationResult(is_valid=True)

        # Validate plan structure
        if not isinstance(plan, dict) or "steps" not in plan:
            result.add_error(
                code=ToolErrorCode.INVALID_INPUT,
                message="Plan must be a dict with 'steps' key",
                context={"plan": str(plan)[:100]},
            )
            return result

        steps = plan["steps"]
        if not isinstance(steps, list):
            result.add_error(
                code=ToolErrorCode.INVALID_INPUT,
                message="Plan 'steps' must be a list",
                context={"steps_type": type(steps).__name__},
            )
            return result

        # Validate max number of steps
        result.total_steps = len(steps)
        if context.max_steps and result.total_steps > context.max_steps:
            result.add_error(
                code=ToolErrorCode.CONSTRAINT_VIOLATION,
                message=f"Plan exceeds max steps limit: {result.total_steps} > {context.max_steps}",
                context={"total_steps": result.total_steps, "max_steps": context.max_steps},
            )

        # Validate each step
        for idx, step in enumerate(steps):
            self._validate_step(step, idx, context, result)

        # Validate total budget
        if context.budget_usd is not None and result.total_cost_usd > context.budget_usd:
            result.add_error(
                code=ToolErrorCode.CONSTRAINT_VIOLATION,
                message=f"Plan exceeds budget: ${result.total_cost_usd:.4f} > ${context.budget_usd:.4f}",
                context={"total_cost": result.total_cost_usd, "budget": context.budget_usd},
            )

        # Validate HITL
        if result.requires_hitl and not context.allow_hitl:
            result.add_error(
                code=ToolErrorCode.FORBIDDEN,
                message="Plan requires HITL approval but HITL is not allowed in this context",
                context={"allow_hitl": context.allow_hitl},
            )

        logger.info(
            "plan_validated",
            user_id=context.user_id,
            session_id=context.session_id,
            is_valid=result.is_valid,
            errors_count=len(result.errors),
            warnings_count=len(result.warnings),
            total_cost_usd=result.total_cost_usd,
            total_steps=result.total_steps,
            requires_hitl=result.requires_hitl,
        )

        return result

    def _validate_step(
        self,
        step: dict[str, Any],
        step_index: int,
        context: ValidationContext,
        result: ValidationResult,
    ) -> None:
        """
        Validate an individual step.

        Args:
            step: Step to validate (format: {"tool": "...", "args": {...}})
            step_index: Step index in the plan
            context: Validation context
            result: ValidationResult to enrich
        """
        # Validate step structure
        if not isinstance(step, dict):
            result.add_error(
                code=ToolErrorCode.INVALID_INPUT,
                message=f"Step {step_index} must be a dict",
                step_index=step_index,
                context={"step_type": type(step).__name__},
            )
            return

        if "tool" not in step:
            result.add_error(
                code=ToolErrorCode.MISSING_REQUIRED_PARAM,
                message=f"Step {step_index} missing required 'tool' field",
                step_index=step_index,
            )
            return

        tool_name = step["tool"]
        args = step.get("args", {})

        # Retrieve tool manifest
        try:
            manifest = self.registry.get_tool_manifest(tool_name)
        except ToolManifestNotFound:
            # Fallback: MCP tools with hallucinated suffix (evolution F2.1/F2.5)
            from src.core.context import (
                strip_hallucinated_mcp_suffix,
                user_mcp_tools_ctx,
            )

            manifest = None

            # 1. Admin MCP: strip suffix and retry central registry
            stripped = strip_hallucinated_mcp_suffix(tool_name)
            if stripped:
                try:
                    manifest = self.registry.get_tool_manifest(stripped)
                    step["tool"] = stripped
                except ToolManifestNotFound:
                    pass

            # 2. User MCP: ContextVar with fuzzy resolve
            if manifest is None:
                user_ctx = user_mcp_tools_ctx.get()
                if user_ctx:
                    manifest = user_ctx.resolve_tool_manifest(tool_name)
                    if manifest and manifest.name != tool_name:
                        step["tool"] = manifest.name

            if manifest is None:
                result.add_error(
                    code=ToolErrorCode.NOT_FOUND,
                    message=f"Tool '{tool_name}' not found in catalogue",
                    step_index=step_index,
                    tool_name=tool_name,
                )
                return

        # Validate parameters
        self._validate_parameters(args, manifest, step_index, result)

        # Validate permissions
        self._validate_permissions(manifest, context, step_index, result)

        # Accumulate cost
        step_cost = manifest.cost.est_cost_usd
        result.total_cost_usd += step_cost

        # Detect HITL
        if manifest.permissions.hitl_required:
            result.requires_hitl = True

    def _validate_parameters(
        self,
        args: dict[str, Any],
        manifest: ToolManifest,
        step_index: int,
        result: ValidationResult,
    ) -> None:
        """
        Validate step parameters against the manifest's ParameterSchema.

        Args:
            args: Arguments provided to the tool
            manifest: Tool's ToolManifest
            step_index: Step index
            result: ValidationResult to enrich
        """
        # Build parameter map by name
        params_by_name = {p.name: p for p in manifest.parameters}
        provided_params = set(args.keys())
        schema_params = set(params_by_name.keys())

        # Check required params
        for param_schema in manifest.parameters:
            if param_schema.required and param_schema.name not in provided_params:
                result.add_error(
                    code=ToolErrorCode.MISSING_REQUIRED_PARAM,
                    message=f"Missing required parameter '{param_schema.name}'",
                    step_index=step_index,
                    tool_name=manifest.name,
                    context={"param": param_schema.name},
                )

        # Check unknown params
        unknown_params = provided_params - schema_params
        for unknown in unknown_params:
            result.add_warning(
                code=ToolErrorCode.INVALID_PARAM_VALUE,
                message=f"Unknown parameter '{unknown}' not in tool schema",
                step_index=step_index,
                tool_name=manifest.name,
                context={"param": unknown},
            )

        # Validate each provided param
        for param_name, param_value in args.items():
            if param_name not in params_by_name:
                continue  # Already reported as unknown

            param_schema = params_by_name[param_name]
            self._validate_parameter_value(
                param_name, param_value, param_schema, step_index, manifest.name, result
            )

    def _validate_parameter_value(
        self,
        param_name: str,
        param_value: Any,
        param_schema: Any,
        step_index: int,
        tool_name: str,
        result: ValidationResult,
    ) -> None:
        """
        Validate a parameter value against its schema.

        Args:
            param_name: Parameter name
            param_value: Provided value
            param_schema: Catalogue ParameterSchema
            step_index: Step index
            tool_name: Tool name
            result: ValidationResult to enrich
        """
        # Skip validation for references ($steps.X.field, $context.X.field)
        if isinstance(param_value, str) and param_value.startswith("$"):
            # Note: reference validation will be done by the orchestrator
            # Accept both $steps.X and $context.X patterns
            return

        # Skip validation for Jinja templates ({% ... %} or {{ ... }})
        # These templates will be evaluated at runtime by parallel_executor and their
        # final type cannot be determined statically.
        # Issue: The planner generates Jinja templates that produce strings
        # but the schema expects an array - coercion will happen at runtime.
        if isinstance(param_value, str) and ("{%" in param_value or "{{" in param_value):
            return

        # Validate basic type
        expected_type = param_schema.type
        actual_type = type(param_value).__name__

        type_mapping = {
            "string": str,
            "integer": int,
            "boolean": bool,
            "array": list,
            "object": dict,
        }

        if expected_type in type_mapping:
            expected_python_type = type_mapping[expected_type]
            if not isinstance(param_value, expected_python_type):
                result.add_error(
                    code=ToolErrorCode.INVALID_PARAM_VALUE,
                    message=f"Parameter '{param_name}' has wrong type: expected {expected_type}, got {actual_type}",
                    step_index=step_index,
                    tool_name=tool_name,
                    context={
                        "param": param_name,
                        "expected_type": expected_type,
                        "actual_type": actual_type,
                    },
                )
                return

        # Validate constraints
        for constraint in param_schema.constraints:
            self._validate_constraint(
                param_name, param_value, constraint, step_index, tool_name, result
            )

    def _validate_constraint(
        self,
        param_name: str,
        param_value: Any,
        constraint: ParameterConstraint,
        step_index: int,
        tool_name: str,
        result: ValidationResult,
    ) -> None:
        """
        Validate a Pydantic constraint.

        Args:
            param_name: Parameter name
            param_value: Provided value
            constraint: ParameterConstraint to validate
            step_index: Step index
            tool_name: Tool name
            result: ValidationResult to enrich
        """
        kind = constraint.kind
        expected = constraint.value

        try:
            if kind == "min_length" and isinstance(param_value, str):
                if len(param_value) < expected:
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' length {len(param_value)} < min {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "length": len(param_value), "min": expected},
                    )

            elif kind == "max_length" and isinstance(param_value, str):
                if len(param_value) > expected:
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' length {len(param_value)} > max {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "length": len(param_value), "max": expected},
                    )

            elif kind == "minimum" and isinstance(param_value, int | float):
                if param_value < expected:
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' value {param_value} < min {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "value": param_value, "min": expected},
                    )

            elif kind == "maximum" and isinstance(param_value, int | float):
                if param_value > expected:
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' value {param_value} > max {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "value": param_value, "max": expected},
                    )

            elif kind == "pattern" and isinstance(param_value, str):
                import re

                if not re.match(expected, param_value):
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' does not match pattern {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "value": param_value, "pattern": expected},
                    )

            elif kind == "enum":
                if param_value not in expected:
                    result.add_error(
                        code=ToolErrorCode.CONSTRAINT_VIOLATION,
                        message=f"Parameter '{param_name}' not in allowed values {expected}",
                        step_index=step_index,
                        tool_name=tool_name,
                        context={"param": param_name, "value": param_value, "allowed": expected},
                    )

        except Exception as e:
            logger.warning(
                "constraint_validation_error",
                param=param_name,
                constraint_kind=kind,
                error=str(e),
            )

    def _validate_permissions(
        self,
        manifest: ToolManifest,
        context: ValidationContext,
        step_index: int,
        result: ValidationResult,
    ) -> None:
        """
        Validate required permissions for a tool.

        Args:
            manifest: Tool's ToolManifest
            context: Validation context
            step_index: Step index
            result: ValidationResult to enrich
        """
        # Validate required scopes
        required_scopes = manifest.permissions.required_scopes
        missing_scopes = set(required_scopes) - set(context.available_scopes)

        if missing_scopes:
            result.add_error(
                code=ToolErrorCode.UNAUTHORIZED,
                message=f"Missing required scopes: {', '.join(missing_scopes)}",
                step_index=step_index,
                tool_name=manifest.name,
                context={
                    "missing_scopes": list(missing_scopes),
                    "required_scopes": required_scopes,
                },
            )

        # Validate allowed roles
        allowed_roles = manifest.permissions.allowed_roles
        if allowed_roles:  # If empty, all roles OK
            user_has_role = any(role in allowed_roles for role in context.user_roles)
            if not user_has_role:
                result.add_error(
                    code=ToolErrorCode.FORBIDDEN,
                    message=f"User role not authorized. Required roles: {', '.join(allowed_roles)}",
                    step_index=step_index,
                    tool_name=manifest.name,
                    context={"user_roles": context.user_roles, "allowed_roles": allowed_roles},
                )


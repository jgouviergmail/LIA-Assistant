    def validate_execution_plan(
        self, plan: ExecutionPlan, context: ValidationContext
    ) -> ValidationResult:
        """
        Validate an ExecutionPlan (DSL Phase 5 format).

        This method supports the new ExecutionPlan format with:
        - Typed ExecutionSteps (TOOL, CONDITIONAL, REPLAN, HUMAN)
        - Explicit dependencies (depends_on)
        - Safe Python conditions
        - Enriched metadata

        Args:
            plan: ExecutionPlan to validate (Pydantic model)
            context: Validation context (user, scopes, budget)

        Returns:
            ValidationResult with is_valid, errors, warnings

        Examples:
            >>> from .plan_schemas import ExecutionPlan, ExecutionStep, StepType
            >>> plan = ExecutionPlan(
            ...     user_id="user123",
            ...     steps=[
            ...         ExecutionStep(
            ...             step_id="step_1",
            ...             step_type=StepType.TOOL,
            ...             agent_name="contacts_agent",
            ...             tool_name="search_contacts_tool",
            ...             parameters={"query": "John"}
            ...         )
            ...     ]
            ... )
            >>> result = validator.validate_execution_plan(plan, context)
            >>> assert result.is_valid
        """
        if not HAS_EXECUTION_PLAN:
            raise ImportError("ExecutionPlan not available. Install plan_schemas module.")

        result = ValidationResult(is_valid=True)

        # Validate max number of steps
        result.total_steps = len(plan.steps)
        if context.max_steps and result.total_steps > context.max_steps:
            result.add_error(
                code=ToolErrorCode.CONSTRAINT_VIOLATION,
                message=f"Plan exceeds max steps limit: {result.total_steps} > {context.max_steps}",
                context={"total_steps": result.total_steps, "max_steps": context.max_steps},
            )

        # Validate each step
        for idx, step in enumerate(plan.steps):
            self._validate_execution_step(step, idx, context, result)

        # Validate dependencies (no cycles)
        self._validate_dependencies(plan.steps, result)

        # Validate references in conditions and parameters
        self._validate_step_references(plan.steps, result)

        # Validate total budget
        if context.budget_usd is not None and result.total_cost_usd > context.budget_usd:
            result.add_error(
                code=ToolErrorCode.CONSTRAINT_VIOLATION,
                message=f"Plan exceeds budget: ${result.total_cost_usd:.4f} > ${context.budget_usd:.4f}",
                context={"total_cost": result.total_cost_usd, "budget": context.budget_usd},
            )

        # Validate HITL
        if result.requires_hitl and not context.allow_hitl:
            result.add_error(
                code=ToolErrorCode.FORBIDDEN,
                message="Plan requires HITL approval but HITL is not allowed in this context",
                context={"allow_hitl": context.allow_hitl},
            )

        logger.info(
            "execution_plan_validated",
            plan_id=plan.plan_id,
            user_id=context.user_id,
            session_id=context.session_id,
            is_valid=result.is_valid,
            errors_count=len(result.errors),
            warnings_count=len(result.warnings),
            total_cost_usd=result.total_cost_usd,
            total_steps=result.total_steps,
            requires_hitl=result.requires_hitl,
        )

        return result

    def _validate_execution_step(
        self,
        step: ExecutionStep,
        step_index: int,
        context: ValidationContext,
        result: ValidationResult,
    ) -> None:
        """
        Validate an ExecutionStep.

        Args:
            step: ExecutionStep to validate
            step_index: Step index in the plan
            context: Validation context
            result: ValidationResult to enrich
        """
        # TOOL steps: validate tool exists and params
        if step.step_type == StepType.TOOL:
            if not step.agent_name:
                result.add_error(
                    code=ToolErrorCode.INVALID_INPUT,
                    message=f"Step {step.step_id}: agent_name required for TOOL steps",
                    step_index=step_index,
                )
                return

            if not step.tool_name:
                result.add_error(
                    code=ToolErrorCode.INVALID_INPUT,
                    message=f"Step {step.step_id}: tool_name required for TOOL steps",
                    step_index=step_index,
                )
                return

            # Retrieve tool manifest
            try:
                manifest = self.registry.get_tool_manifest(step.tool_name)
            except ToolManifestNotFound:
                # Fallback: MCP tools with hallucinated suffix (evolution F2.1/F2.5)
                from src.core.context import (
                    strip_hallucinated_mcp_suffix,
                    user_mcp_tools_ctx,
                )

                manifest = None

                # 1. Admin MCP: strip suffix and retry central registry
                stripped = strip_hallucinated_mcp_suffix(step.tool_name)
                if stripped:
                    try:
                        manifest = self.registry.get_tool_manifest(stripped)
                        step.tool_name = stripped
                    except ToolManifestNotFound:
                        pass

                # 2. User MCP: ContextVar with fuzzy resolve
                if manifest is None:
                    user_ctx = user_mcp_tools_ctx.get()
                    if user_ctx:
                        manifest = user_ctx.resolve_tool_manifest(step.tool_name)
                        if manifest and manifest.name != step.tool_name:
                            step.tool_name = manifest.name

                if manifest is None:
                    result.add_error(
                        code=ToolErrorCode.NOT_FOUND,
                        message=f"Tool '{step.tool_name}' not found in catalogue",
                        step_index=step_index,
                        tool_name=step.tool_name,
                    )
                    return

            # Validate parameters (skip $steps.X references)
            self._validate_step_parameters(step.parameters, manifest, step_index, result)

            # Validate permissions
            self._validate_permissions(manifest, context, step_index, result)

            # Accumulate cost
            step_cost = manifest.cost.est_cost_usd
            result.total_cost_usd += step_cost

            # Detect HITL
            # FIX 2026-01-30: Skip plan_approval for FOR_EACH steps
            # They get confirmation via for_each_confirmation which provides better UX
            # with real item data from pre-execution (count + previews)
            if manifest.permissions.hitl_required or step.approvals_required:
                if not step.is_for_each_step:
                    result.requires_hitl = True

        # CONDITIONAL steps: validate condition is safe
        elif step.step_type == StepType.CONDITIONAL:
            if not step.condition:
                result.add_error(
                    code=ToolErrorCode.INVALID_INPUT,
                    message=f"Step {step.step_id}: condition required for CONDITIONAL steps",
                    step_index=step_index,
                )
                return

            # Validate that the condition is safe (AST whitelist)
            self._validate_condition_safe(step.condition, step.step_id, step_index, result)

        # HUMAN steps: require HITL approval (supported for confirmation workflows)
        elif step.step_type == StepType.HUMAN:
            result.requires_hitl = True

        # REPLAN: not supported in MVP
        elif step.step_type == StepType.REPLAN:
            result.add_error(
                code=ToolErrorCode.NOT_IMPLEMENTED,
                message=f"Step {step.step_id}: {step.step_type} not supported in MVP (Phase 2)",
                step_index=step_index,
                context={"step_type": step.step_type.value},
            )

    def _validate_step_parameters(
        self,
        args: dict[str, Any],
        manifest: ToolManifest,
        step_index: int,
        result: ValidationResult,
    ) -> None:
        """
        Validate TOOL step parameters (excluding $steps references).

        Args:
            args: Arguments provided to the tool
            manifest: Tool's ToolManifest
            step_index: Step index
            result: ValidationResult to enrich
        """
        # Build parameter map by name
        params_by_name = {p.name: p for p in manifest.parameters}
        provided_params = set(args.keys())
        schema_params = set(params_by_name.keys())

        # Check required params (skip if it's a $steps.X or $context.X reference)
        for param_schema in manifest.parameters:
            if param_schema.required:
                param_value = args.get(param_schema.name)
                # Skip validation if reference
                if param_value is None:
                    result.add_error(
                        code=ToolErrorCode.MISSING_REQUIRED_PARAM,
                        message=f"Missing required parameter '{param_schema.name}'",
                        step_index=step_index,
                        tool_name=manifest.name,
                        context={"param": param_schema.name},
                    )
                elif isinstance(param_value, str) and param_value.startswith("$"):
                    # Reference ($steps.X or $context.X): will be validated at execution time
                    continue

        # Check unknown params
        unknown_params = provided_params - schema_params
        for unknown in unknown_params:
            result.add_warning(
                code=ToolErrorCode.INVALID_PARAM_VALUE,
                message=f"Unknown parameter '{unknown}' not in tool schema",
                step_index=step_index,
                tool_name=manifest.name,
                context={"param": unknown},
            )

        # Validate constraints for each provided param (type, min_length, etc.)
        # Critical: without this validation, empty strings pass for required params
        for param_name, param_value in args.items():
            if param_name not in params_by_name:
                continue  # Already reported as unknown

            # Skip validation for references (validated at execution time)
            if isinstance(param_value, str) and param_value.startswith("$"):
                continue

            param_schema = params_by_name[param_name]
            self._validate_parameter_value(
                param_name, param_value, param_schema, step_index, manifest.name, result
            )

    def _validate_condition_safe(
        self, condition: str, step_id: str, step_index: int, result: ValidationResult
    ) -> None:
        """
        Validate that a condition is safe (AST whitelist).

        Verifies that the condition only contains safe operations:
        - Comparisons: ==, !=, <, >, <=, >=
        - Booleans: and, or, not
        - Lengths: len()
        - Field access: $steps.X.field

        Args:
            condition: Python expression to validate
            step_id: Step ID
            step_index: Step index
            result: ValidationResult to enrich
        """
        import ast
        import re

        # Replace $steps.X with safe variable for AST parsing
        # Support both numeric indices ($steps.0) and named step IDs ($steps.search_contacts)
        safe_condition = re.sub(
            r"\$steps\.[a-zA-Z_][a-zA-Z0-9_]*(\.[a-zA-Z_][a-zA-Z0-9_]*)*", "var", condition
        )

        try:
            tree = ast.parse(safe_condition, mode="eval")
        except SyntaxError as e:
            result.add_error(
                code=ToolErrorCode.INVALID_INPUT,
                message=f"Invalid condition syntax: {e}",
                step_index=step_index,
                context={"condition": condition, FIELD_STEP_ID: step_id},
            )
            return

        # Whitelist of allowed AST nodes
        allowed_nodes = {
            ast.Expression,  # Wrapper node from ast.parse(mode="eval")
            ast.Compare,
            ast.BoolOp,
            ast.UnaryOp,
            ast.Call,  # For len()
            ast.Name,
            ast.Constant,
            ast.Attribute,
            ast.Subscript,
            ast.Load,
            ast.Eq,
            ast.NotEq,
            ast.Lt,
            ast.LtE,
            ast.Gt,
            ast.GtE,
            ast.And,
            ast.Or,
            ast.Not,
            ast.In,
            ast.NotIn,
        }

        for node in ast.walk(tree):
            if type(node) not in allowed_nodes:
                result.add_error(
                    code=ToolErrorCode.FORBIDDEN,
                    message=f"Unsafe node in condition: {node.__class__.__name__}",
                    step_index=step_index,
                    context={"condition": condition, "unsafe_node": node.__class__.__name__},
                )
                return

            # Verify that Call is only len()
            if isinstance(node, ast.Call):
                if isinstance(node.func, ast.Name) and node.func.id != "len":
                    result.add_error(
                        code=ToolErrorCode.FORBIDDEN,
                        message=f"Only len() function allowed in conditions, got: {node.func.id}",
                        step_index=step_index,
                        context={"condition": condition, "function": node.func.id},
                    )
                    return

    def _validate_dependencies(self, steps: list[ExecutionStep], result: ValidationResult) -> None:
        """
        Validate that there are no cyclic dependencies.

        Args:
            steps: List of steps
            result: ValidationResult to enrich
        """
        # Create dependency graph
        step_ids = {step.step_id for step in steps}
        graph: dict[str, list[str]] = {step.step_id: step.depends_on for step in steps}

        # Verify that all dependencies exist
        for step in steps:
            for dep in step.depends_on:
                if dep not in step_ids:
                    result.add_error(
                        code=ToolErrorCode.INVALID_INPUT,
                        message=f"Step {step.step_id} depends on non-existent step: {dep}",
                        context={FIELD_STEP_ID: step.step_id, "missing_dependency": dep},
                    )

        # Detect cycles (DFS)
        visited = set()
        rec_stack = set()

        def has_cycle(node: str) -> bool:
            visited.add(node)
            rec_stack.add(node)

            for neighbor in graph.get(node, []):
                if neighbor not in visited:
                    if has_cycle(neighbor):
                        return True
                elif neighbor in rec_stack:
                    return True

            rec_stack.remove(node)
            return False

        for step_id in step_ids:
            if step_id not in visited:
                if has_cycle(step_id):
                    result.add_error(
                        code=ToolErrorCode.INVALID_INPUT,
                        message="Cyclic dependency detected in plan steps",
                        context={"graph": graph},
                    )
                    break

    def _validate_step_references(
        self, steps: list[ExecutionStep], result: ValidationResult
    ) -> None:
        """
        Validate that all $steps.X references in conditions and parameters
        point to steps that exist and will be executed BEFORE.

        Detects issues such as:
        - References to non-existent steps
        - Circular references (step referencing itself)
        - Forward references (step referencing a future step not yet executed)

        Args:
            steps: List of steps to validate
            result: ValidationResult to enrich
        """
        import re

        # Create a step_id -> index mapping for order validation
        step_indices = {step.step_id: idx for idx, step in enumerate(steps)}
        step_ids = set(step_indices.keys())

        # Pattern to extract $steps.X references
        # Captures the step_id after "$steps."
        steps_reference_pattern = re.compile(r"\$steps\.([a-zA-Z_][a-zA-Z0-9_]*)")

        # Pattern to extract $context.X references
        # Captures: $context.contacts.0.resource_name, $context.emails[0].id
        # Valid domains: contacts, emails, calendar, tasks, drive
        context_reference_pattern = re.compile(r"\$context\.([a-zA-Z_][a-zA-Z0-9_]*)")
        valid_context_domains = {"contacts", "emails", "events", "tasks", "drive"}

        for idx, step in enumerate(steps):
            # Collect all strings to analyze for this step
            strings_to_check = []

            # 1. Condition for CONDITIONAL steps
            if step.step_type == StepType.CONDITIONAL and step.condition:
                strings_to_check.append(("condition", step.condition))

            # 2. Parameters for TOOL steps
            if step.step_type == StepType.TOOL and step.parameters:
                for param_name, param_value in step.parameters.items():
                    if isinstance(param_value, str):
                        strings_to_check.append((f"parameter '{param_name}'", param_value))

            # 3. on_success and on_fail for CONDITIONAL steps
            # Phase 5.2A: Strict validation with forbidden values
            if step.step_type == StepType.CONDITIONAL:
                # List of forbidden values (generic placeholders)
                forbidden_step_ids = {
                    "done",
                    "error",
                    "end",
                    "success",
                    "fail",
                    "complete",
                    "finished",
                    "abort",
                    "stop",
                }

                if step.on_success:
                    # Check forbidden values (ONLY if step doesn't exist)
                    # Allow forbidden placeholders if they match an actual step_id
                    # Example: step_id="error" is allowed, "error" as generic placeholder is not
                    if step.on_success in forbidden_step_ids and step.on_success not in step_ids:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: on_success uses forbidden placeholder '{step.on_success}'. "
                                f"Must reference an actual step_id that exists in steps array. "
                                f"Forbidden values: {', '.join(sorted(forbidden_step_ids))}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "forbidden_value": step.on_success,
                                "available_steps": list(step_ids),
                            },
                        )
                    # Verify that the referenced step exists
                    elif step.on_success not in step_ids:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: on_success references "
                                f"non-existent step '{step.on_success}'. "
                                f"Available steps: {', '.join(sorted(step_ids))}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "missing_target": step.on_success,
                                "available_steps": list(step_ids),
                            },
                        )

                if step.on_fail:
                    # Check forbidden values (ONLY if step doesn't exist)
                    # Allow forbidden placeholders if they match an actual step_id
                    # Example: step_id="error" is allowed, "error" as generic placeholder is not
                    if step.on_fail in forbidden_step_ids and step.on_fail not in step_ids:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: on_fail uses forbidden placeholder '{step.on_fail}'. "
                                f"Must reference an actual step_id that exists in steps array. "
                                f"Forbidden values: {', '.join(sorted(forbidden_step_ids))}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "forbidden_value": step.on_fail,
                                "available_steps": list(step_ids),
                            },
                        )
                    # Verify that the referenced step exists
                    elif step.on_fail not in step_ids:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: on_fail references "
                                f"non-existent step '{step.on_fail}'. "
                                f"Available steps: {', '.join(sorted(step_ids))}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "missing_target": step.on_fail,
                                "available_steps": list(step_ids),
                            },
                        )

            # Analyze all collected strings
            for source, string_value in strings_to_check:
                # Extract all $steps.X references
                steps_matches = steps_reference_pattern.findall(string_value)

                for referenced_step_id in steps_matches:
                    # Verify that the referenced step exists
                    if referenced_step_id not in step_ids:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: {source} references "
                                f"non-existent step: {referenced_step_id}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "source": source,
                                "missing_reference": referenced_step_id,
                            },
                        )
                        continue

                    # Check for circular reference
                    if referenced_step_id == step.step_id:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: {source} has circular reference to itself"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "source": source,
                            },
                        )
                        continue

                    # Check execution order (no forward reference)
                    referenced_idx = step_indices[referenced_step_id]
                    if referenced_idx >= idx:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: {source} references "
                                f"step {referenced_step_id} that will be executed "
                                f"later (index {referenced_idx} >= {idx})"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "referenced_step_id": referenced_step_id,
                                "referenced_step_index": referenced_idx,
                                "source": source,
                            },
                        )

                # Extract all $context.X references
                context_matches = context_reference_pattern.findall(string_value)

                for referenced_domain in context_matches:
                    # Verify that the context domain is valid
                    if referenced_domain not in valid_context_domains:
                        result.add_error(
                            code=ToolErrorCode.INVALID_INPUT,
                            message=(
                                f"Step {step.step_id}: {source} references "
                                f"invalid context domain: {referenced_domain}. "
                                f"Valid domains: {', '.join(sorted(valid_context_domains))}"
                            ),
                            context={
                                FIELD_STEP_ID: step.step_id,
                                "step_index": idx,
                                "source": source,
                                "invalid_domain": referenced_domain,
                                "valid_domains": list(valid_context_domains),
                            },
                        )

        # Phase 2.3 - Issue #32: Validate field paths against tool schemas
        self._validate_reference_field_paths(steps, result)

    def _validate_reference_field_paths(
        self, steps: list[ExecutionStep], result: ValidationResult
    ) -> None:
        """
        Validate field paths in $steps.X.field.path references against tool schemas.

        Phase 2.3 - Issue #32: Pre-Execution Validation

        This validation catches schema mismatches BEFORE tool execution, preventing
        costly API calls for invalid references.

        Common errors detected:
        - ❌ $steps.search.emails[0].value → Field is 'emailAddresses' not 'emails'
        - ❌ $steps.details.phones[0] → Field is 'phoneNumbers' not 'phones'
        - ❌ $steps.search.results[0] → No 'results' field (use 'contacts')

        Args:
            steps: List of ExecutionSteps to validate
            result: ValidationResult to populate with errors

        Algorithm:
            1. Build mapping step_id -> tool_name
            2. For each step:
               a. Collect parameters and condition
               b. Extract $steps references
               c. Validate field paths against tool schemas
            3. Add errors to result

        Examples:
            >>> # Valid reference
            >>> steps = [
            ...     ExecutionStep(
            ...         step_id="search",
            ...         step_type=StepType.TOOL,
            ...         tool_name="search_contacts_tool",
            ...         parameters={"query": "John"}
            ...     ),
            ...     ExecutionStep(
            ...         step_id="send",
            ...         step_type=StepType.TOOL,
            ...         tool_name="send_email_tool",
            ...         parameters={
            ...             "to": "$steps.search.contacts[0].emailAddresses[0].value"
            ...         }
            ...     )
            ... ]
            >>> validator._validate_reference_field_paths(steps, result)
            >>> assert result.is_valid  # Valid path
            >>>
            >>> # Invalid reference (wrong field name)
            >>> steps[1].parameters["to"] = "$steps.search.contacts[0].emails[0].value"
            >>> validator._validate_reference_field_paths(steps, result)
            >>> assert not result.is_valid  # Invalid - 'emails' doesn't exist
        """
        from src.domains.agents.orchestration.reference_validator import ReferenceValidator

        # Build mapping step_id -> tool_name for schema lookup
        step_tools: dict[str, str] = {}
        for step in steps:
            if step.step_type == StepType.TOOL and step.tool_name:
                step_tools[step.step_id] = step.tool_name

        # Create validator instance
        validator = ReferenceValidator()

        # Track all reference errors
        all_ref_errors: list = []

        # Validate each step
        for idx, step in enumerate(steps):
            # Only validate TOOL and CONDITIONAL steps
            if step.step_type not in [StepType.TOOL, StepType.CONDITIONAL]:
                continue

            # Collect parameters and condition
            parameters = step.parameters if step.step_type == StepType.TOOL else {}
            condition = step.condition if step.step_type == StepType.CONDITIONAL else None

            # Validate references in this step
            ref_errors = validator.validate_references_in_step(
                step_id=step.step_id,
                step_index=idx,
                parameters=parameters,
                step_tools=step_tools,
                condition=condition,
            )

            # Add errors to result and tracking list
            for ref_error in ref_errors:
                all_ref_errors.append(ref_error)
                result.add_error(
                    code=ref_error.code,
                    message=ref_error.message,
                    step_index=ref_error.step_index,
                    tool_name=ref_error.tool_name,
                    context={
                        FIELD_STEP_ID: ref_error.step_id,
                        "parameter_name": ref_error.parameter_name,
                        "reference": ref_error.reference,
                        "invalid_path": ref_error.invalid_path,
                        **(ref_error.context or {}),
                    },
                )

        # Log validation completion
        if all_ref_errors:
            logger.info(
                "reference_field_path_validation_complete",
                errors_count=len(all_ref_errors),
                steps_count=len(steps),
                message=f"Found {len(all_ref_errors)} field path validation errors",
            )



@dataclass
class ValidationContext:
    """
    Validation context for a plan.

    Contains the information needed to validate:
    - Permissions (available OAuth scopes, user roles)
    - Budget (max allowed cost)
    - Session (user_id, session_id for audit)

    Attributes:
        user_id: User identifier
        session_id: Session identifier
        available_scopes: List of available OAuth scopes
        user_roles: List of user roles
        budget_usd: Max budget in USD (None = no limit)
        allow_hitl: If False, rejects tools requiring HITL
        max_steps: Max number of allowed steps (None = no limit)

    Examples:
        >>> context = ValidationContext(
        ...     user_id="user123",
        ...     session_id="sess456",
        ...     available_scopes=["google_contacts.read"],
        ...     budget_usd=1.0
        ... )
    """

    user_id: str
    session_id: str
    available_scopes: list[str] = field(default_factory=list)
    user_roles: list[str] = field(default_factory=list)
    budget_usd: float | None = None
    allow_hitl: bool = True
    max_steps: int | None = None


# ============================================================================
# Validation Results
# ============================================================================


@dataclass
class ValidationIssue:
    """
    Validation issue (error or warning).

    Attributes:
        severity: "error" or "warning"
        code: Standardized error code
        message: Descriptive message
        step_index: Index of the affected step (None if global)
        tool_name: Name of the affected tool (None if not applicable)
        context: Additional context for debugging

    Examples:
        >>> issue = ValidationIssue(
        ...     severity="error",
        ...     code=ToolErrorCode.MISSING_REQUIRED_PARAM,
        ...     message="Parameter 'query' is required",
        ...     step_index=2,
        ...     tool_name="search_contacts_tool",
        ...     context={"param": "query"}
        ... )
    """

    severity: str  # "error" | "warning"
    code: ToolErrorCode
    message: str
    step_index: int | None = None
    tool_name: str | None = None
    context: dict[str, Any] = field(default_factory=dict)


@dataclass
class ValidationResult:
    """
    Validation result for a plan.

    Attributes:
        is_valid: True if the plan is valid (no errors)
        errors: List of blocking errors
        warnings: List of non-blocking warnings
        total_cost_usd: Estimated total cost of the plan
        total_steps: Total number of steps
        requires_hitl: If True, at least one step requires HITL
        validated_at: Validation timestamp

    Examples:
        >>> result = ValidationResult(
        ...     is_valid=True,
        ...     errors=[],
        ...     warnings=[],
        ...     total_cost_usd=0.05,
        ...     total_steps=3,
        ...     requires_hitl=False
        ... )
    """

    is_valid: bool
    errors: list[ValidationIssue] = field(default_factory=list)
    warnings: list[ValidationIssue] = field(default_factory=list)
    total_cost_usd: float = 0.0
    total_steps: int = 0
    requires_hitl: bool = False
    validated_at: datetime = field(default_factory=lambda: datetime.now(UTC))

    def add_error(
        self,
        code: ToolErrorCode,
        message: str,
        step_index: int | None = None,
        tool_name: str | None = None,
        context: dict[str, Any] | None = None,
    ) -> None:
        """Add an error and mark the result as invalid."""
        self.errors.append(
            ValidationIssue(
                severity="error",
                code=code,
                message=message,
                step_index=step_index,
                tool_name=tool_name,
                context=context or {},
            )
        )
        self.is_valid = False

    def add_warning(
        self,
        code: ToolErrorCode,
        message: str,
        step_index: int | None = None,
        tool_name: str | None = None,
        context: dict[str, Any] | None = None,
    ) -> None:
        """Add a warning without invalidating the result."""
        self.warnings.append(
            ValidationIssue(
                severity="warning",
                code=code,
                message=message,
                step_index=step_index,
                tool_name=tool_name,
                context=context or {},
            )
        )



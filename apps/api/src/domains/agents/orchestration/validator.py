"""
Validator for agent execution plans.

This module validates plans generated by the planner before their execution by the orchestrator.
It relies on catalogue manifests (Phase 1) to ensure:
- Parameters conform to ParameterSchema
- Sufficient permissions (OAuth scopes, roles)
- Acceptable total cost (budget, limits)
- HITL (Human-In-The-Loop) when required

Architecture:
- PlanValidator: Main validation class
- ValidationResult: Validation result (success, errors, warnings)
- ValidationError: Validation error with context

Usage:
    from .validator import PlanValidator, ValidationContext

    validator = PlanValidator(registry)
    context = ValidationContext(
        user_id="user123",
        session_id="sess456",
        available_scopes=["google_contacts.read"],
        budget_usd=1.0
    )

    result = validator.validate_plan(plan, context)
    if not result.is_valid:
        raise ValidationError(result.errors)

Compliance: LangGraph v1.0 + declarative catalogue Phase 1
"""

